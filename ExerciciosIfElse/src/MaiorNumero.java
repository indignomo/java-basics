public class MaiorNumero {

    public void verificaMaior(int a, int b){
        if(a == b){
            System.out.println("Números iguais.");
        } else if (a > b){
            System.out.println(a + " é o maior número.");
        } else {
            System.out.println(b + " é o maior número.");
        }
    }

}
